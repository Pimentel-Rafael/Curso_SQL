-- Quantidade de transações acumuladas ao longo do tempo (Diario)?

WITH

tb_diario AS (

SELECT  substr(DtCriacao,1,10) AS dtDia,
        count(DISTINCT IdTransacao) AS qtTransacao

FROM transacoes

GROUP BY dtDia
ORDER BY dtDia

)

SELECT  *,
        sum(qtTransacao) OVER (ORDER BY dtDia) AS qtTransacaoAcum

FROM tb_diario

